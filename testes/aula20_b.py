def titulo(txt):    # definindo a função titulo() passando a ela um PARÂMETRO
    print('-'*30)
    print(txt)      # é exibido o valor desse parâmetro
    print('-'*30)


""" PROGRAMA PRINCIPAL """
titulo(f'{'CURSO EM VÍDEO':^30}')   # chamando a função e passando o valor do parâmetro
titulo(f'{'APRENDENDO FUNÇÕES':^30}')   # chamando a função e passando o valor do parâmetro
titulo(f'{'MATHEUS HENRIQUE':^30}')   # chamando a função e passando o valor do parâmetro
