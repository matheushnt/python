""" DECLARANDO UM DICIONÁRIO """
pessoa = {'nome': 'Joana',
          'idade': 45,
          'sexo': 'F'}
print(pessoa.keys())    # retorna as chaves do dicionário
print(pessoa.values)    # retorna os valores das chaves do dicionário
print(pessoa.items())   # retorna o os itens (conjunto chave-valor) do dicionário 
