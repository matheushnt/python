metros = int(input('Digite um valor em METROS: '))  #lê a entrada de dados
centimetros = metros * 100  # multiplica por 100
milimetros = metros * 1000  # multiplica por 1000
print('{} metros convertidos em centímetros é {} cm e convertidos em milímetros é {} mm'.format(metros , centimetros, milimetros))  # exibe a resposta